def basicpacketcheck (pkt):
    if len(pkt) < 20:
        return 1
    if int(pkt[0] >> 4) != 4:
        return 2
    
    pkt_sum = 0
    for i in range(20):
        temp = pkt[i] 
        pkt_sum += temp*2**(i*8)
    
    while pkt_sum > 0xFFFF:
        pkt_sum0 = pkt_sum & 0xFFFF
        pkt_sum1 = pkt_sum >> 16
        pkt_sum = pkt_sum0 + pkt_sum1
    
    if pkt_sum != 0xFFFF:
        return 3
    
    totallength = ((pkt[2]*2**8) + pkt[3])
    if totallength != len(pkt):
        return 4
    
    return True


